vowelA = "a"
vowelE = "e"
vowelO = "o"
while True:
    string = input("Please type in a string:")
    if vowelA in string:
        print("a found")
    else:
        print("a not found")
    if vowelE in string:
        print("e found")
    else: 
        print("e not found")
    if vowelO in string:
        print("o found")
    else:
        print("o not found")
    break    
